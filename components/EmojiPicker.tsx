// Emoji Picker Component
const EmojiPicker: React.FC<{
    onEmojiSelect: (emoji: string) => void;
    onClose: () => void;
}> = ({ onEmojiSelect, onClose }) => {
    const [activeCategory, setActiveCategory] = useState<string>('Smileys');
    const pickerRef = useRef<HTMLDivElement>(null);

    useEffect(() => {
        const handleClickOutside = (e: MouseEvent) => {
            if (pickerRef.current && !pickerRef.current.contains(e.target as Node)) {
                onClose();
            }
        };
        document.addEventListener('mousedown', handleClickOutside);
        return () => document.removeEventListener('mousedown', handleClickOutside);
    }, [onClose]);

    const categories = Object.keys(EMOJI_DATA);

    return (
        <motion.div
            ref={pickerRef}
            initial={{ opacity: 0, scale: 0.9, y: 10 }}
            animate={{ opacity: 1, scale: 1, y: 0 }}
            exit={{ opacity: 0, scale: 0.9, y: 10 }}
            transition={{ type: "spring", stiffness: 400, damping: 25 }}
            className="absolute bottom-full left-0 mb-2 bg-black/90 backdrop-blur-2xl border border-white/20 rounded-2xl shadow-2xl overflow-hidden z-[9999]"
            style={{ width: '360px', height: '320px' }}
        >
            <div className="flex h-full">
                {/* Category Sidebar */}
                <div className="w-20 bg-white/5 border-r border-white/10 p-2 flex flex-col gap-1 overflow-y-auto custom-scrollbar">
                    {categories.map((category) => (
                        <button
                            key={category}
                            onClick={() => setActiveCategory(category)}
                            className={`px-2 py-2 rounded-lg text-[10px] font-medium transition-all whitespace-nowrap ${activeCategory === category
                                ? 'bg-white/20 text-white'
                                : 'text-white/50 hover:bg-white/10 hover:text-white/80'
                                }`}
                            title={category}
                        >
                            {category}
                        </button>
                    ))}
                </div>

                {/* Emoji Grid */}
                <div className="flex-1 p-3 overflow-y-auto custom-scrollbar">
                    <div className="grid grid-cols-8 gap-1">
                        {EMOJI_DATA[activeCategory as keyof typeof EMOJI_DATA].map((emoji, idx) => (
                            <button
                                key={idx}
                                onClick={() => {
                                    onEmojiSelect(emoji);
                                    onClose();
                                }}
                                className="w-8 h-8 flex items-center justify-center text-xl hover:bg-white/10 rounded-lg transition-colors"
                                title={emoji}
                            >
                                {emoji}
                            </button>
                        ))}
                    </div>
                </div>
            </div>

            {/* Close button */}
            <button
                onClick={onClose}
                className="absolute top-2 right-2 p-1.5 text-white/40 hover:text-white hover:bg-white/10 rounded-lg transition-colors z-10"
            >
                <X size={14} />
            </button>
        </motion.div>
    );
};

export default EmojiPicker;
